@asynccontextmanager
async def lifespan(app: FastAPI):
    """Manage application lifecycle"""
    global http_client, gemini_model
    
    # Initialize HTTP client
    http_client = httpx.AsyncClient(
        timeout=httpx.Timeout(30.0, connect=5.0),
        limits=httpx.Limits(
            max_keepalive_connections=20,
            max_connections=100
        )
    )
    
    # Initialize Vertex AI
    try:
        vertexai.init(
            project=settings.gcp_project_id,
            location=settings.gcp_region
        )
        gemini_model = GenerativeModel(settings.gemini_model)
        print(f"‚úÖ Initialized Vertex AI Gemini model: {settings.gemini_model}")
        print(f"‚úÖ Project: {settings.gcp_project_id}, Region: {settings.gcp_region}")
    except Exception as e:
        print(f"‚ùå Failed to initialize Vertex AI: {str(e)}")
        print("Will fall back to mock responses for development")
        settings.mock_mode = True
    
    yield
    
    # Shutdown
    if http_client:
        await http_client.aclose()

